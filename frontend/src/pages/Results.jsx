import { useContext, useEffect } from "react";
import { Link, useNavigate } from "react-router-dom";
import { motion } from "framer-motion";
import { CheckCircle, TrendingUp, DollarSign, ArrowRight } from "lucide-react";

import { PredictionContext } from "../context/PredictionContext";
import PageWrapper from "../components/PageWrapper";

export default function Results() {
  const { predictionResult } = useContext(PredictionContext);
  const navigate = useNavigate();

  useEffect(() => {
    if (!predictionResult) {
      navigate("/assessment", { replace: true });
    }
  }, [predictionResult, navigate]);

  if (!predictionResult) {
    return null;
  }

  const features = predictionResult.features || {};

  const debtRatio = features.debt_to_income_ratio || 0;
  const burdenRatio = features.loan_burden_ratio || 0;

  const riskScore = predictionResult.risk_score ?? 0;
  const creditScore = predictionResult.credit_score ?? 0;
  const probability = predictionResult.probability ?? 0;
  const riskLevel = predictionResult.risk_level ?? "Unknown";

  let riskColor = "text-emerald-700";
  let riskBg = "bg-emerald-100";
  let riskBorder = "border-emerald-200";

  if (riskLevel === "High Risk") {
    riskColor = "text-red-700";
    riskBg = "bg-red-100";
    riskBorder = "border-red-200";
  } else if (riskLevel === "Medium Risk") {
    riskColor = "text-amber-700";
    riskBg = "bg-amber-100";
    riskBorder = "border-amber-200";
  }

  const metricCards = [
    {
      title: "Debt-to-Income Ratio",
      value: debtRatio.toFixed(2),
      description: "Monthly debt obligations relative to income",
      icon: DollarSign,
    },
    {
      title: "Loan Burden Ratio",
      value: burdenRatio.toFixed(2),
      description: "Loan payment impact on your finances",
      icon: TrendingUp,
    },
  ];

  return (
    <PageWrapper>
      <div className="min-h-screen bg-slate-50 py-10">
        <div className="mx-auto max-w-7xl px-4 sm:px-6 lg:px-8">
          {/* Header */}

          <motion.div
            initial={{ opacity: 0, y: 24 }}
            animate={{ opacity: 1, y: 0 }}
            className="mb-8"
          >
            <p className="text-sm font-semibold uppercase tracking-wide text-blue-600">
              Credit Assessment
            </p>

            <h1 className="mt-2 text-4xl font-bold text-slate-900">
              Your Credit Analysis Report
            </h1>

            <p className="mt-3 max-w-3xl text-lg text-slate-600">
              Review your financial indicators, risk profile, and personalized
              recommendations generated by CreditWise AI.
            </p>
          </motion.div>

          {/* Hero Card */}

          <motion.div
            initial={{ opacity: 0, y: 24 }}
            animate={{ opacity: 1, y: 0 }}
            transition={{ delay: 0.1 }}
            className={`mb-8 rounded-3xl border ${riskBorder} bg-white p-8 shadow-lg`}
          >
            <div className="flex flex-col gap-8 lg:flex-row lg:items-center lg:justify-between">
              <div>
                <div
                  className={`inline-flex items-center gap-2 rounded-full px-4 py-2 text-sm font-semibold ${riskBg} ${riskColor}`}
                >
                  <CheckCircle size={18} />
                  {riskLevel}
                </div>

                <h2 className="mt-5 text-3xl font-bold text-slate-900">
                  Credit Score: {creditScore}/100
                </h2>
                <p className="mt-3 text-slate-600">
                  Default Probability: {(probability * 100).toFixed(2)}%
                </p>

                <p className="mt-3 max-w-2xl text-slate-600">
                  This result is designed to support financial decisions and
                  should be used alongside additional underwriting information.
                </p>
              </div>

              {/* Risk Score */}

              <div className="flex flex-col items-center">
                <div className="relative flex h-36 w-36 items-center justify-center rounded-full bg-slate-100">
                  <div className="absolute h-28 w-28 rounded-full bg-white shadow-inner" />

                  <div className="z-10 text-center">
                    <p className="text-4xl font-bold text-slate-900">
                      {riskScore}
                    </p>

                    <p className="text-sm text-slate-500">Risk Score</p>
                  </div>
                </div>
              </div>
            </div>
          </motion.div>

          {/* Metrics */}

          <div className="grid gap-6 md:grid-cols-2">
            {metricCards.map((card, index) => {
              const Icon = card.icon;

              return (
                <motion.div
                  key={card.title}
                  initial={{ opacity: 0, y: 24 }}
                  animate={{ opacity: 1, y: 0 }}
                  transition={{ delay: index * 0.1 + 0.2 }}
                  whileHover={{ y: -4 }}
                  className="rounded-3xl border border-slate-200 bg-white p-6 shadow-sm"
                >
                  <div className="flex items-start justify-between">
                    <div>
                      <p className="text-sm font-medium text-slate-500">
                        {card.title}
                      </p>

                      <h3 className="mt-3 text-4xl font-bold text-slate-900">
                        {card.value}
                      </h3>

                      <p className="mt-2 text-sm text-slate-500">
                        {card.description}
                      </p>
                    </div>

                    <div className="rounded-2xl bg-blue-100 p-3">
                      <Icon className="h-6 w-6 text-blue-600" />
                    </div>
                  </div>
                </motion.div>
              );
            })}
          </div>

          {/* Explanation Section */}

          <motion.div
            initial={{ opacity: 0, y: 24 }}
            animate={{ opacity: 1, y: 0 }}
            transition={{ delay: 0.4 }}
            className="mt-8 rounded-3xl border border-slate-200 bg-white p-8 shadow-sm"
          >
            <h3 className="text-xl font-bold text-slate-900">
              Understanding Your Results
            </h3>

            <div className="mt-6 grid gap-6 md:grid-cols-2">
              <div className="rounded-2xl bg-slate-50 p-5">
                <h4 className="font-semibold text-slate-900">
                  Debt-to-Income Ratio
                </h4>

                <p className="mt-2 text-sm leading-6 text-slate-600">
                  Lower values indicate stronger repayment capacity and
                  healthier financial stability.
                </p>
              </div>

              <div className="rounded-2xl bg-slate-50 p-5">
                <h4 className="font-semibold text-slate-900">
                  Loan Burden Ratio
                </h4>

                <p className="mt-2 text-sm leading-6 text-slate-600">
                  This metric estimates how much of your income is consumed by
                  loan obligations.
                </p>
              </div>
            </div>
          </motion.div>

          {/* CTA */}

          <motion.div
            initial={{ opacity: 0 }}
            animate={{ opacity: 1 }}
            transition={{ delay: 0.5 }}
            className="mt-10 flex flex-col gap-4 sm:flex-row"
          >
            <Link
              to="/recommendations"
              className="inline-flex items-center justify-center gap-2 rounded-2xl bg-blue-600 px-8 py-4 font-semibold text-white shadow-lg transition hover:bg-blue-700"
            >
              View Personalized Recommendations
              <ArrowRight size={18} />
            </Link>

            <Link
              to="/assessment"
              className="inline-flex items-center justify-center rounded-2xl border border-slate-300 px-8 py-4 font-semibold text-slate-700 transition hover:bg-slate-100"
            >
              Run Another Assessment
            </Link>
          </motion.div>
        </div>
      </div>
    </PageWrapper>
  );
}
